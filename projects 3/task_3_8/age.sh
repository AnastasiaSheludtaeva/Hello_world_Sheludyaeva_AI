#!/bin/bash
# Запрашиваем имя и возраст
read -p "Введите ваше имя: " NAME
read -p "Введите ваш возраст (лет): " AGE

# Проверяем, что возраст - это число (простая проверка)
if [[ "$AGE" =~ ^[0-9]+$ ]]; then
  # Арифметическая операция
  AGE_IN_DAYS=$((AGE * 365))
  echo "$NAME, вы прожили примерно $AGE_IN_DAYS дней."
else
  echo "Ошибка: возраст должен быть числом."
fi
