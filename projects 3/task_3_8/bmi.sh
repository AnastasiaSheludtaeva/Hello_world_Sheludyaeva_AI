#!/bin/bash
read -p "Введите массу тела (кг): " WEIGHT
read -p "Введите рост (метры, например 1.75): " HEIGHT

# Проверяем, что введены числа
if [[ "$WEIGHT" =~ ^[0-9]+([.][0-9]+)?$ ]] && [[ "$HEIGHT" =~ ^[0-9]+([.][0-9]+)?$ ]]; then
  # Используем bc для вычислений с плавающей точкой
  BMI=$(echo "scale=0; $WEIGHT / ($HEIGHT * $HEIGHT)" | bc)
  echo "Ваш индекс массы тела (ИМТ): $BMI"
else
  echo "Ошибка: введите корректные числа (можно использовать точку для дробной части)."
fi